package com.ruyicai.mgr.controller.dto;

import org.springframework.roo.addon.javabean.RooJavaBean;
import org.springframework.roo.addon.json.RooJson;

@RooJson
@RooJavaBean
public class CaseLotCelebrityDTO {
	private Long id;
	private String text;
	private String userno;
	private String lotno;
	private Integer sortno;

	public CaseLotCelebrityDTO(Long id, String text, String userno, String lotno, Integer sortno) {
		super();
		this.id = id;
		this.text = text;
		this.userno = userno;
		this.lotno = lotno;
		this.sortno = sortno;
	}
}
