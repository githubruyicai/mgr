package com.ruyicai.mgr.domain;

import java.io.Serializable;

import javax.persistence.Column;

import org.springframework.roo.addon.entity.RooIdentifier;
import org.springframework.roo.addon.tostring.RooToString;

@RooIdentifier
@RooToString
public class TlotmapPK implements Serializable {
	private static final long serialVersionUID = 1L;
	@Column(name = "agencyno")
	private String agencyno;
	@Column(name = "agencyflowno")
	private String agencyflowno;

	public String getAgencyno() {
		return agencyno;
	}

	public void setAgencyno(String agencyno) {
		this.agencyno = agencyno;
	}

	public String getAgencyflowno() {
		return agencyflowno;
	}

	public void setAgencyflowno(String agencyflowno) {
		this.agencyflowno = agencyflowno;
	}

}